Dayoftheweek = int(input("Input number between 1 and 7: "))

if Dayoftheweek == 1:
    print('Monday')
elif Dayoftheweek == 2:
    print('Tuesday')
elif Dayoftheweek == 3:
    print('Wednesday')
elif Dayoftheweek == 4:
    print('Thursday')
elif Dayoftheweek == 5:
    print('Friday')
elif Dayoftheweek == 6:
    print('Saturday')
elif Dayoftheweek == 7:
    print('Sunday')
else:
    print('Invalid input. Please enter a number between 1 and 7.')
                            
